    <h3>INCONTINÊNCIA URINÁRIA E PROLAPSO GENITAL</h3>

    <p>Uroginecologia é a subespecialidade que alia a Urologia à Ginecologia
      para cuidar de condições adversas da saúde feminina, como incontinência
      urinária e prolapso genital.</br></br>
      A incontinência urinária é a perda involuntária de urina. Acomete mais as
      mulheres do que os homens em uma relação de 2:1, causando grande
      impacto na autoestima e qualidade de vida.</br></br>
      Os tipos mais comuns de incontinência urinária são: a) de esforço, que
      está associada a situações de aumento da pressão intra abdominal como
      atividade física, tosse e espirro; b) de urgência, que está relacionada a um
      forte desejo de urinar; c) misto, que é a associação das duas primeiras
      modalidades de incontinência.</br>
      O prolapso genital ou distopia genital consiste no “desarranjo” anatômico
      dos órgãos pélvicos causado pela perda de sua sustentação muscular
      e/ou ligamentar, resultando na protusão dos mesmos para o canal vaginal
      ou mesmo para o exterior da vagina dependendo da sua graduação.</br></br>
      Os fatores de risco mais importantes para o prolapso genital são a idade,
      gravidez, deficiência hormonal (hipoestrogenismo), cirurgia pélvica prévia,
      história familiar e deficiência de colágeno/elastina.</br>
      Os sintomas podem ser isolados ou associados dependendo do(s)
      órgão(s) prolapsado e do grau do prolapso. Dor perineal ou pélvica,
      sensação de “peso” ou “bola vaginal", sintomas urinários irritativos e/ou
      obstrutivos, podendo estar associados a incontinência urinária e
      constipação ("intestino preso"), o que compromete o bem-estar físico,
      emocional, social e sexual das mulheres.</br></br>
      O tratamento das distopias genitais visa o reposicionamento dos órgãos
      pélvicos prolapsados e deve ser particularizado e individualizado,
      dependendo das queixas e expectativas, presença ou ausência de vida
      sexual ativa e risco cirúrgico. Este reposicionamento pode ser feito
      através de dispositivos intravaginais (pessários) ou correção cirúrgica com
      acesso via vaginal ou abdominal.</br></br>
      Atualmente, a correção cirúrgica do prolapso via abdominal pode ser feita
      através de acesso minimamente invasivo (através de “pequenos
      orifícios”), com uso de plataforma robótica, o que resultou em maior
      precisão com menos morbidade cirúrgica. A vantagem do acesso via
      abdominal em relação a via vaginal é a eficácia e possibilidade de
      reconstruir toda a anatomia pélvica (correção global), ou seja, de todos os
      compartimentos prolapsados (anterior, médio e posterior) sem o risco de
      encurtamento vaginal e menor possibilidade de dor pélvica durante a
      relação sexual (dispareunia).
    </p>
